def safe_bar_chart(
    data: pd.DataFrame,
    x_col: str,
    y_col: str,
    title: str,
    y_label: str,
) -> None:
    chart_data = data[[x_col, y_col]].dropna().set_index(x_col)
    st.markdown(f"#### {title}")
    st.bar_chart(chart_data, y=y_col)
    st.caption(y_label)


def dataframe_download_button(
    df: pd.DataFrame,
    label: str,
    file_name: str,
) -> None:
    csv = df.to_csv(index=True).encode("utf-8")
    st.download_button(
        label=label,
        data=csv,
        file_name=file_name,
        mime="text/csv",
    )


