@st.cache_data(show_spinner=False)
def load_csv(path: Path, index_col: Optional[int] = 0) -> Optional[pd.DataFrame]:
    """Load a CSV file safely. Return None if the file does not exist."""
    if not path.exists():
        return None
    try:
        return pd.read_csv(path, index_col=index_col)
    except Exception as exc:
        st.warning(f"Could not load {path.name}: {exc}")
        return None


def format_number(value: float) -> str:
    if pd.isna(value):
        return "N/A"
    return f"{value:,.2f}"


def format_percent(value: float) -> str:
    if pd.isna(value):
        return "N/A"
    return f"{value:.2%}"


def metric_value(df: Optional[pd.DataFrame], metric_name: str) -> Optional[float]:
    if df is None or df.empty:
        return None
    if "metric" not in df.columns or "value" not in df.columns:
        return None

    matched = df.loc[df["metric"] == metric_name, "value"]
    if matched.empty:
        return None

    try:
        return float(matched.iloc[0])
    except Exception:
        return None


